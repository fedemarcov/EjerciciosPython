# LEER 2 NUMEROS Y REALIZAR LA SUMA

numero1 = input("Ingrese el numero: ")
numero1 = int(numero1)

numero2 = input("Ingrese un numero: ")
numero2 = int(numero2)

suma = numero1 + numero2
print("Suma: ", suma)

# INGRESE OTRO NUMERO PARA DIVIDIR EL ANTERIOR

numero3 = input("Ingrese un numero: ")
numero3 = int(numero3)
x = suma / numero3

print ("Resultado: ", x)